x = 90
y = 40

z= x + y
print("The value of z is:", z)                  #addition

z = x - y
print("The value of z is:", z)                  #subtraction

z = x * y
print("The value of z is:", z)                  #multiplication

z = x / y
print("The value of z is:", z)                  #division

z = x % y
print("The value of z is:", z)                  #modulus divides then gives output as remainder

z = x ** y
print("The value of z is:", float(z))                 #exponential power 90power40

z = x // y
print("The value of z is:", z)                 #floor division where values after decimal are removed

